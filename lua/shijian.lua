-- ganzhi month
function GanZhiLi:calGanZhiMonthNum()
    if self.ttime < self.jqs[1] then
        return nil
    end
    local x = 0
    if self.ttime < self.jqs[1] then
        return nil
    end -- error wrong year
    for i = 1, 23 do
        if self.jqs[i] <= self.ttime and self.jqs[i + 1] > self.ttime then
            x = i
        end
    end
    if x == 0 then
        x = 24
    end
    return math.floor((x + 1) / 2)
end

