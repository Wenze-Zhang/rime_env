--===== angle conversion ===============
local rad = 180 * 3600 / math.pi -- arc seconds per radian
local RAD = 180 / math.pi        -- degrees per radian
function int2(v)                 -- integer part
    v = math.floor(v)
    if v < 0 then
        return v + 1
    else
        return v
    end
end

function rad2mrad(v) -- normalize angle to 0 to 2pi
    v = math.fmod(v, 2 * math.pi)
    if v < 0 then
        return v + 2 * math.pi
    else
        return v
    end
end

function rad2str(d, tim) -- radians to string
    ---tim=0 sample output -23deg 59m 48.23s
    ---tim=1 sample output 18h 29m 44.52s
    local s = "+"
    local w1 = "°"
    w2 = "’"
    w3 = "”"
    if d < 0 then
        d = -d
        s = '-'
    end
    if tim ~= 0 then
        d = d * 12 / math.pi
        w1 = "h "
        w2 = "m "
        w3 = "s "
    else
        d = d * 180 / math.pi
    end
    local a = math.floor(d)
    d = (d - a) * 60
    local b = math.floor(d)
    d = (d - b) * 60
    local c = math.floor(d)
    d = (d - c) * 100
    d = math.floor(d + 0.5)
    if d >= 100 then
        d = d - 100
        c = c + 1
    end
    if c >= 60 then
        c = c - 60
        b = b + 1
    end
    if b >= 60 then
        b = b - 60
        a = a + 1
    end
    a = "   " + a
    b = "0" + b
    c = "0" + c
    d = "0" + d
    local alen = string.len(a)
    local blen = string.len(b)
    local clen = string.len(c)
    local dlen = string.len(d)
    s = s .. string.sub(a, alen - 3, alen) + w1
    s = s .. string.sub(b, blen - 2, blen) + w2
    s = s .. string.sub(c, clen - 2, clen) + "."
    s = s .. string.sub(d, dlen - 2, dlen) + w3
    return s
end

