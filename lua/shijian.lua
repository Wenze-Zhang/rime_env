--=============== sun position ===================
local EnnT = 0  -- set EnnT before calling Enn
function Enn(F) -- evaluate one group of periodic or poisson terms set EnnT first
    local i
    local v = 0
    for i = 1, #F, 3 do
        v = v + F[i] * math.cos(F[i + 1] + EnnT * F[i + 2])
        -- log.info('Fsize=' .. #F, 'i=' .. i, 'v='..v, 'F[i]='..F[i], 'm='..math.cos(F[i+1]+EnnT*F[i+2]))
    end
    return v
end

function earCal(jd) -- earth position heliocentric date ecliptic coordinates
    EnnT = jd / 365250
    -- log.info('EnnT=' .. EnnT)
    local llr = {}
    local t1 = EnnT
    local t2 = t1 * t1
    local t3 = t2 * t1
    local t4 = t3 * t1
    local t5 = t4 * t1
    -- log.info('t1='..t1, 't2='..t2, 't3='..t3, 't4='..t4, 't5='..t5)
    llr[1] = Enn(E10) + Enn(E11) * t1 + Enn(E12) * t2 + Enn(E13) * t3 + Enn(E14) * t4 + Enn(E15) * t5
    -- log.info('sppp')
    llr[2] = Enn(E20) + Enn(E21) * t1
    -- log.info('eppp')
    llr[3] = Enn(E30) + Enn(E31) * t1 + Enn(E32) * t2 + Enn(E33) * t3
    llr[1] = rad2mrad(llr[1])
    -- log.info('llr[0]='..llr[1], 'llr[1]='..llr[2], 'llr[2]='..llr[3])
    return llr
end

function sunCal2(jd) -- apparent geocentric solar longitude and latitude at jd
    local sun = earCal(jd)
    sun[1] = sun[1] + math.pi
    sun[2] = -sun[2]                  -- true sun position
    local d = nutation(jd)
    sun[1] = rad2mrad(sun[1] + d.Lon) -- add nutation
    addGxc(jd, sun)                   -- add annual aberration
    return sun                        -- return apparent sun position
end

--=============== moon position ===================
local MnnT = 0  -- set MnnT before calling Mnn
function Mnn(F) -- evaluate M10 M11 M20 set MnnT first
    local i
    local v = 0
    local t1 = MnnT
    local t2 = t1 * t1
    local t3 = t2 * t1
    local t4 = t3 * t1
    for i = 1, #F, 6 do
        v = v + F[i] * math.sin(F[i + 1] + t1 * F[i + 2] + t2 * F[i + 3] + t3 * F[i + 4] + t4 * F[i + 5])
    end
    return v
end

function moonCal(jd) -- moon position geocentric date ecliptic coordinates
    MnnT = jd / 36525
    local t1 = MnnT
    local t2 = t1 * t1
    local t3 = t2 * t1
    local t4 = t3 * t1
    local llr = {}
    llr[1] = (Mnn(M10) + Mnn(M11) * t1 + Mnn(M12) * t2) / rad
    llr[2] = (Mnn(M20) + Mnn(M21) * t1) / rad
    llr[3] = (Mnn(M30) + Mnn(M31) * t1) * 0.999999949827
    llr[1] = llr[1] + M1n[1] + M1n[2] * t1 + M1n[3] * t2 + M1n[4] * t3 + M1n[5] * t4
    llr[1] = rad2mrad(llr[1]) -- geocentric date ecliptic without precession
    addPrece(jd, llr)         -- add precession
    return llr
end

function moonCal2(jd) -- apparent geocentric lunar longitude and latitude
    local moon = moonCal(jd)
    local d = nutation(jd)
    moon[1] = rad2mrad(moon[1] + d.Lon) -- add nutation
    return moon
end

