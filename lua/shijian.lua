local GXC_e = { 0.016708634, -0.000042037, -0.0000001267 }                                                              -- eccentricity
local GXC_p = { 102.93735 / RAD, 1.71946 / RAD, 0.00046 / RAD }                                                         -- perihelion
local GXC_l = { 280.4664567 / RAD, 36000.76982779 / RAD, 0.0003032028 / RAD, 1 / 49931000 / RAD, -1 / 153000000 / RAD } -- solar mean longitude
local GXC_k = 20.49552 /
    rad                                                                                                                 -- aberration constant
function addGxc(t, zb)                                                                                                  -- annual aberration in ecliptic coordinates
    local t1 = t / 36525
    local t2 = t1 * t1
    local t3 = t2 * t1
    local t4 = t3 * t1
    local L = GXC_l[1] + GXC_l[2] * t1 + GXC_l[3] * t2 + GXC_l[4] * t3 + GXC_l[5] * t4
    local p = GXC_p[1] + GXC_p[2] * t1 + GXC_p[3] * t2
    local e = GXC_e[1] + GXC_e[2] * t1 + GXC_e[3] * t2
    local dL = L - zb[1]
    local dP = p - zb[1]
    zb[1] = zb[1] - (GXC_k * (math.cos(dL) - e * math.cos(dP)) / math.cos(zb[2]))
    zb[2] = zb[2] - (GXC_k * math.sin(zb[2]) * (math.sin(dL) - e * math.sin(dP)))
    -- log.info('aa', L,p,e,dL,dP,zb[1], zb[2])
    zb[1] = rad2mrad(zb[1])
end

